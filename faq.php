	<h3>¿Los cursos entregan certificado de asistencia?</h3>
	<div>En la página del portafolio de CECEP Virtual aparecen relacionados los talleres, cursos, seminarios y diplomados en donde se muestra la descripción de cada uno de ellos y esta informa a su vez, Sí incluye el certificado o No lo incluye.</div>
	<h3>¿Quién avala los certificados?</h3>
	<div>Los certificados son avalados y firmados por la Fundación Centro Colombiano de Estudios Profesionales FCECEP. <a href="https://www.cecep.edu.co" target="_blank">www.cecep.edu.co</a></div>
	<h3>¿Cómo puedo inscribirme en un curso y hacer el pago?</h3>
	<div>Cada curso tiene su opción de inscripción y al inscribirse usted recibirá un correo con las opciones de pago, incluyendo el botón de pago por PSE.</div>
	<h3>¿Cuanto tiempo tengo que estar conectado si me matriculo en un curso?</h3>
	<div>Cada curso tiene un número estimado de horas y un número de módulos que lo conforman. Esta información se muestra en la descripción de cada curso. Cada participante se conecta y avanza en los módulo a su propio ritmo y puede terminar antes o despues del número de horas estimadas para el curso. Todo depende de usted mismo.<br>
	También es posible hacerlo de forma presencial en las instalaciones de la Fcecep usando los canales de pago establecidos y, una vez validado el pago, se habilitan las claves para el ingreso al curso escogido.<br>
	El valor de los programas es en pesos colombianos y varía según el tipo de formación, el número de horas, el tipo de certificación y los plazos de pago.  Revisar todos estos puntos en el momento de la inscripción.</div>
	<h3>¿A qué público están dirigidos los cursos?</h3>
	<div>Cada curso tiene definido su público objetivo el cual se encuentra en la descripción inicial del curso.</div>
	<h3>Si pierdo el usuario o la contraseña, ¿Cómo puedo recuparar esos datos?</h3>
	<div>Nuestros estudiantes virtuales cuentan con el apoyo de soporte técnico. El cual atiende solicitudes desde el correo: soporte@cecepvirtual.edu.co</div>	
	<h3>Cuando escribo al correo de soporte o a un docente virtual ¿Cual es el tiempo estimado de respuesta?</h3>
	<div>Nuestros docentes virtuales y soporte técnico deben responder las solicitudes en un tiempo no mayor a 24 horas, contadas a partir de la hora y fecha del recibido.</div>
	<h3>¿Desde que dispositivo puedo acceder a los cursos?</h3>
	<div>Computador, celular, tablet o cualquier otro dispositivo que permita el acceso a Internet.</div>	
	<h3>Para acceder a la plataforma virtual ¿Es necesario una velocidad requerida de Internet?</h3>
	<div>Con 512 kb de velocidad de Internet es suficiente para conectarse a la plataforma; pero estaría navegando con el recurso mínimo y lo estaría haciendo de forma lenta. Aunque es bien sabido que todos los proveedores actuales de Internet cuentan con Planes de conexión superiores a esta velocidad; por lo que no tendrá problema de velocidad en términos generales desde el plan mas básico.</div>
	<h3>¿Qué tipo de hardware o software se requiere para acceder a la plataforma?</h3>
	<div>Desde un Computador, celular, tablet o cualquier otro dispositivo que permita el acceso a Internet puede entrar a la plataforma. Para el buen desempeño e interacción con los recursos utilizados es necesario que estos dispositivos cuenten con parlantes o sonido para escuchar los podcast (audios) o vídeos publicados en los cursos.</div>
	<h3>¿Los docentes vituales ofrecen tutorías?</h3>
	<div>Si, algunos docentes y dependiendo del curso; ofrecen tutorías por video conferencia utilizando Skype, Meet, Zoom o Teams en horarios concertados. Sin desconocer que el correo electrónico también es el principal medio de comunicación y consulta con sus tutores o docentes virtuales.</div>
	<h3>Los trabajos o actividades ¿Tienen una fecha estimada de entrega?</h3>
	<div>Los cursos disponibles los realizan los estudiantes a su propio ritmo al igual que las entregas; pero algunos cursos tienen módulos programados según calendario y estos requieren que se entreguen actividades o se participe en los foros en unas fechas requeridas. Depende del curso y todo será informado en la descripción del mismo.</div>	
	
